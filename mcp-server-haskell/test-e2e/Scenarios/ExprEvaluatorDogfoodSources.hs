-- | The test-suite body: 18 unit checks + 3 QuickCheck properties.
-- The 'Arbitrary Expr' generator restricts 'Var' names to a shape
-- the parser accepts — widening it would require a matching parser
-- extension, and 'prop_prettyRoundtrip' would start failing.
specSrc :: Text
specSrc = T.unlines
  [ "module Main where"
  , ""
  , "import qualified Data.Map.Strict as Map"
  , "import Expr.Eval      (eval)"
  , "import Expr.Pretty    (parse, pretty)"
  , "import Expr.Simplify  (simplify)"
  , "import Expr.Syntax    (Env, Error, Expr (..))"
  , "import System.Exit    (exitFailure, exitSuccess)"
  , "import Test.QuickCheck"
  , "  ( Arbitrary (..)"
  , "  , Gen"
  , "  , Property"
  , "  , Result (..)"
  , "  , elements"
  , "  , forAll"
  , "  , listOf"
  , "  , oneof"
  , "  , quickCheckWithResult"
  , "  , sized"
  , "  , stdArgs"
  , "  , (===)"
  , "  )"
  , "import qualified Test.QuickCheck as QC"
  , ""
  , "instance Arbitrary Expr where"
  , "  arbitrary = sized go"
  , "    where"
  , "      go :: Int -> Gen Expr"
  , "      go n"
  , "        | n <= 0    = oneof [ Lit <$> arbitrary, Var <$> genVar ]"
  , "        | otherwise = oneof"
  , "            [ Lit <$> arbitrary"
  , "            , Var <$> genVar"
  , "            , Neg <$> go (n `div` 2)"
  , "            , Add <$> go (n `div` 2) <*> go (n `div` 2)"
  , "            , Mul <$> go (n `div` 2) <*> go (n `div` 2)"
  , "            ]"
  , "  shrink (Lit n)   = [ Lit 0 | n /= 0 ]"
  , "  shrink (Var _)   = []"
  , "  shrink (Neg e)   = e : [ Neg e' | e' <- shrink e ]"
  , "  shrink (Add a b) = [a, b] <> [ Add a' b  | a' <- shrink a ]"
  , "                            <> [ Add a  b' | b' <- shrink b ]"
  , "  shrink (Mul a b) = [a, b] <> [ Mul a' b  | a' <- shrink a ]"
  , "                            <> [ Mul a  b' | b' <- shrink b ]"
  , ""
  , "genVar :: Gen String"
  , "genVar = do"
  , "  c  <- elements alphas"
  , "  cs <- listOf (elements alnums)"
  , "  pure (c : cs)"
  , "  where"
  , "    alphas = ['a' .. 'z'] <> ['A' .. 'Z']"
  , "    alnums = alphas <> ['0' .. '9']"
  , ""
  , "genEnv :: Gen Env"
  , "genEnv = do"
  , "  pairs <- listOf ((,) <$> genVar <*> arbitrary)"
  , "  pure (Map.fromList pairs)"
  , ""
  , "prop_prettyRoundtrip :: Expr -> Property"
  , "prop_prettyRoundtrip e = parse (pretty e) === Just e"
  , ""
  , "prop_simplifyPreservesMeaning :: Expr -> Property"
  , "prop_simplifyPreservesMeaning e = forAll genEnv $ \\env ->"
  , "  case (eval env e :: Either Error Integer, eval env (simplify e)) of"
  , "    (Right n, simplified) -> simplified === Right n"
  , "    (Left  _, _)          -> QC.property ()"
  , ""
  , "prop_simplifyIdempotent :: Expr -> Property"
  , "prop_simplifyIdempotent e = simplify (simplify e) === simplify e"
  , ""
  , "runProp :: QC.Testable prop => String -> prop -> IO Bool"
  , "runProp name prop = do"
  , "  res <- quickCheckWithResult stdArgs { QC.chatty = False } prop"
  , "  let ok = case res of Success {} -> True; _ -> False"
  , "  putStrLn ((if ok then \"PASS  \" else \"FAIL  \") <> name)"
  , "  pure ok"
  , ""
  , "main :: IO ()"
  , "main = do"
  , "  propResults <- sequence"
  , "    [ runProp \"prop_prettyRoundtrip\"           prop_prettyRoundtrip"
  , "    , runProp \"prop_simplifyPreservesMeaning\"  prop_simplifyPreservesMeaning"
  , "    , runProp \"prop_simplifyIdempotent\"        prop_simplifyIdempotent"
  , "    ]"
  , "  if and propResults then exitSuccess else exitFailure"
  ]
