evalSrc :: Text
evalSrc = T.unlines
  [ "module Expr.Eval (eval) where"
  , ""
  , "import Expr.Syntax"
  , ""
  , "eval :: Env -> Expr -> Either Error Integer"
  , "eval env = go"
  , "  where"
  , "    go :: Expr -> Either Error Integer"
  , "    go (Lit n)   = Right n"
  , "    go (Var x)   = lookupVar x env"
  , "    go (Neg e)   = negate <$> go e"
  , "    go (Add a b) = (+) <$> go a <*> go b"
  , "    go (Mul a b) = (*) <$> go a <*> go b"
  ]

simplifySrc :: Text
simplifySrc = T.unlines
  [ "module Expr.Simplify (simplify) where"
  , ""
  , "import Expr.Syntax"
  , ""
  , "simplify :: Expr -> Expr"
  , "simplify (Neg e) ="
  , "  case simplify e of"
  , "    Neg inner -> inner"
  , "    Lit n     -> Lit (negate n)"
  , "    e'        -> Neg e'"
  , "simplify (Add a b) ="
  , "  case (simplify a, simplify b) of"
  , "    (Lit 0, y)     -> y"
  , "    (x, Lit 0)     -> x"
  , "    (Lit m, Lit n) -> Lit (m + n)"
  , "    (x, y)         -> Add x y"
  , "simplify (Mul a b) ="
  , "  case (simplify a, simplify b) of"
  , "    (Lit 0, _)     -> Lit 0"
  , "    (_, Lit 0)     -> Lit 0"
  , "    (Lit 1, y)     -> y"
  , "    (x, Lit 1)     -> x"
  , "    (Lit m, Lit n) -> Lit (m * n)"
  , "    (x, y)         -> Mul x y"
  , "simplify e = e"
  ]

-- | The pretty/parser pair. The two design decisions hidden here —
-- never paren-wrap 'Neg', and only treat @(-digits)@ as a literal
-- when the trailing token is @)@ — are the pins that stop the
-- @prop_prettyRoundtrip@ QuickCheck law from finding either of the
-- two counterexamples the original dogfood surfaced.
prettySrc :: Text
prettySrc = T.unlines
  [ "module Expr.Pretty (pretty, parse) where"
  , ""
  , "import Data.Char (isAlpha, isAlphaNum, isDigit, isSpace)"
  , "import Expr.Syntax"
  , ""
  , "pretty :: Expr -> String"
  , "pretty = go 0"
  , "  where"
  , "    go :: Int -> Expr -> String"
  , "    go _ (Lit n)"
  , "      | n < 0     = \"(\" <> show n <> \")\""
  , "      | otherwise = show n"
  , "    go _ (Var x)   = x"
  , "    go _ (Neg e)   = \"-\" <> go 3 e"
  , "    go p (Add a b) = parenIf (p > 0) (go 0 a <> \" + \" <> go 1 b)"
  , "    go p (Mul a b) = parenIf (p > 1) (go 1 a <> \" * \" <> go 2 b)"
  , "    parenIf :: Bool -> String -> String"
  , "    parenIf True  s = \"(\" <> s <> \")\""
  , "    parenIf False s = s"
  , ""
  , "parse :: String -> Maybe Expr"
  , "parse s = case pExpr (skipWS s) of"
  , "  Just (e, rest) | all isSpace rest -> Just e"
  , "  _                                 -> Nothing"
  , ""
  , "type P a = String -> Maybe (a, String)"
  , ""
  , "pExpr :: P Expr"
  , "pExpr s0 = case pTerm s0 of"
  , "  Nothing       -> Nothing"
  , "  Just (t0, r0) -> chainAdd t0 r0"
  , "  where"
  , "    chainAdd acc s = case skipWS s of"
  , "      '+' : rest -> case pTerm (skipWS rest) of"
  , "        Just (t, s') -> chainAdd (Add acc t) s'"
  , "        Nothing      -> Nothing"
  , "      _ -> Just (acc, s)"
  , ""
  , "pTerm :: P Expr"
  , "pTerm s0 = case pFactor s0 of"
  , "  Nothing       -> Nothing"
  , "  Just (f0, r0) -> chainMul f0 r0"
  , "  where"
  , "    chainMul acc s = case skipWS s of"
  , "      '*' : rest -> case pFactor (skipWS rest) of"
  , "        Just (f, s') -> chainMul (Mul acc f) s'"
  , "        Nothing      -> Nothing"
  , "      _ -> Just (acc, s)"
  , ""
  , "pFactor :: P Expr"
  , "pFactor s = case skipWS s of"
  , "  '-' : rest -> case pFactor (skipWS rest) of"
  , "    Just (e, s') -> Just (Neg e, s')"
  , "    Nothing      -> Nothing"
  , "  s'         -> pAtom s'"
  , ""
  , "pAtom :: P Expr"
  , "pAtom s0 = case skipWS s0 of"
  , "  '(' : rest -> case pInsideParens (skipWS rest) of"
  , "    Just (e, afterInner) -> case skipWS afterInner of"
  , "      ')' : afterClose -> Just (e, afterClose)"
  , "      _                -> Nothing"
  , "    Nothing              -> Nothing"
  , "  s' -> case s' of"
  , "    c : _"
  , "      | isDigit c -> pUnsignedLit s'"
  , "      | isAlpha c -> pIdent s'"
  , "    _             -> Nothing"
  , ""
  , "pInsideParens :: P Expr"
  , "pInsideParens s = case s of"
  , "  '-' : rest | isSoleNegLit rest -> pNegLit s"
  , "  _                              -> pExpr s"
  , "  where"
  , "    isSoleNegLit :: String -> Bool"
  , "    isSoleNegLit s0 = case span isDigit s0 of"
  , "      (digits, after) ->"
  , "        not (null digits)"
  , "          && case skipWS after of"
  , "               ')' : _ -> True"
  , "               _       -> False"
  , ""
  , "pNegLit :: P Expr"
  , "pNegLit ('-' : rest) ="
  , "  case span isDigit rest of"
  , "    (digits, after) | not (null digits) ->"
  , "      case readIntegerSafe digits of"
  , "        Just n  -> Just (Lit (negate n), after)"
  , "        Nothing -> Nothing"
  , "    _ -> Nothing"
  , "pNegLit _ = Nothing"
  , ""
  , "pUnsignedLit :: P Expr"
  , "pUnsignedLit s = case span isDigit s of"
  , "  (digits, rest) | not (null digits) ->"
  , "    case readIntegerSafe digits of"
  , "      Just n  -> Just (Lit n, rest)"
  , "      Nothing -> Nothing"
  , "  _ -> Nothing"
  , ""
  , "pIdent :: P Expr"
  , "pIdent (c : rest) | isAlpha c ="
  , "  let (tailPart, rest') = span isAlphaNum rest"
  , "  in Just (Var (c : tailPart), rest')"
  , "pIdent _ = Nothing"
  , ""
  , "skipWS :: String -> String"
  , "skipWS = dropWhile isSpace"
  , ""
  , "readIntegerSafe :: String -> Maybe Integer"
  , "readIntegerSafe []     = Nothing"
  , "readIntegerSafe digits = go 0 digits"
  , "  where"
  , "    go acc []       = Just acc"
  , "    go acc (c : cs)"
  , "      | isDigit c   = go (acc * 10 + toInteger (fromEnum c - fromEnum '0')) cs"
  , "      | otherwise   = Nothing"
  ]

