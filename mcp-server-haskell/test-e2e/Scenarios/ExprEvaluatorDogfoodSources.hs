-- | Haskell sources inlined verbatim from the dogfood of
-- @playground/expr-evaluator@. Kept in its own module so
-- 'Scenarios.FlowExprEvaluatorDogfood' stays readable — the sources
-- are long and we don't want them drowning the orchestration.
--
-- These are the /correct/ sources (i.e. with the two pretty-parser
-- fixes and the refined simplify law baked in). If any of them stops
-- compiling, stops passing the tests, or stops surviving the parser
-- roundtrip, the scenario fails and the regression is obvious.
module Scenarios.ExprEvaluatorDogfoodSources
  ( syntaxSrc
  , evalSrc
  , simplifySrc
  , prettySrc
